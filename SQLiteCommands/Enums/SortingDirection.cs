namespace SQLiteCommands.Enums;

/// <summary>
/// The sorting order for a specified field.
/// </summary>
public enum SortingDirection
{
    /// <summary>
    /// Indicates that the data should be sorted from the smallest to the biggest values.
    /// </summary>
    Ascending,

    /// <summary>
    /// Indicates that the data should be sorted from the biggest to the smallest values.
    /// </summary>
    Descending
}